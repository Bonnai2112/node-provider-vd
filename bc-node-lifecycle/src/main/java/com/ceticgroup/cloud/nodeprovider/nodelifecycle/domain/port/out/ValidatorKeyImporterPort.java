package com.ceticgroup.cloud.nodeprovider.nodelifecycle.domain.port.out;

import com.ceticgroup.cloud.nodeprovider.nodelifecycle.domain.DeploymentRef;
import java.util.List;

public interface ValidatorKeyImporterPort {

    /**
     * Imports the given keystores into the deployment so the consensus client/validator can sign
     * with the corresponding keys. Returns the BLS pubkeys (0x-prefixed, 96 hex chars) that were
     * successfully imported.
     */
    List<String> importKeystores(DeploymentRef ref, List<KeystoreFile> keystores, String password);

    /**
     * Triggers {@code ethd keys import} for keystores already present in the workdir (e.g. just
     * generated by {@link ValidatorKeyGeneratorPort}). Writes the password file but does not write
     * any keystore.
     */
    void triggerImport(DeploymentRef ref, String password);

    record KeystoreFile(String fileName, String content) {}
}
